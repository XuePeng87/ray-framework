package cc.xuepeng.ray.framework.core.common.consts;

/**
 * 文件的常量类
 *
 * @author xuepeng
 */
public class FileConst {

    /**
     * 构造函数
     */
    private FileConst() {}

    /**
     * KB大小的文件
     */
    public static final String SIZE_KB = "KB";

    /**
     * MB大小的文件
     */
    public static final String SIZE_MB = "MB";

    /**
     * GB大小的文件
     */
    public static final String SIZE_GB = "GB";

    /**
     * 定义KB的计算常量
     */
    public static final int KB = 1024;

    /**
     * 定义MB的计算常量
     */
    public static final int MB = 1024 * 1024;

    /**
     * 定义GB的计算常量
     */
    public static final int GB = 1024 * 1024 * 1024;

    /**
     * 文件后缀分割符。
     */
    public static final String SEPARATOR_FILE_SUFFIX = ".";

    /**
     * 文件路径分隔符
     */
    public static final String SLASH = "/";

    /**
     * 反斜杠
     */
    public static final String BACK_SLASH = "\\";

    /**
     * 连字符
     */
    public static final String HYPHEN = "-";

    /**

     * 宽
     */
    public static final String WIDTH = "width";
    /**
     * 高
     */
    public static final String HEIGHT = "height";

    /**
     * 双引号
     */
    public static final String DOUBLE_QUOTATION_MARK = "\"";

    /**
     * 点号
     */
    public static final String FULL_STOP_LINE = ".";

    /**
     * 符号 "^" 表示异或操作
     */
    public static final String XOR_SYMBOL = "^";

}
